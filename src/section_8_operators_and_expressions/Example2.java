package section_8_operators_and_expressions;

public class Example2 {
    public static void main(String[] args) {

        int a = 12;
        int b = 5;

        int q = a / b;
        System.out.println(q);

        int r = a % b;
        System.out.println(r);

        float fNum1 = 123.23f;
        float fNum2 = 43.5f;
        float fRem = fNum1 % fNum2;
        System.out.println(fRem);
    }
}
