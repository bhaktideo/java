package section_8_operators_and_expressions;

public class Example3 {
    public static void main(String[] args) {

        byte b1 = 10;
        short s1 = 2300;

        int sum = b1 + s1;

        float f1 = 12.13f;
        long l1 = 12345L;
        float fSum = l1 + f1;

        float f2 = 32.21f;
        double d2 = 12.65432;
        double dSum = d2 + f2;

        char cCharacter = 99;
        int nNextValue = 2;
        int nResult = cCharacter + nNextValue;
    }
}
